% RTR:
% rtrdsvd   - RTR driver for computing the dominant singular spaces of a
%             rectangular matrix
% rtreig    - RTR driver for computing the eigenvalue decomposition of a 
%             symmetric matrix (uses exponential map)
% rtreig2   - RTR driver for computing the eigenvalue decomposition of a
%             symmetric matrix (does not use exponential map)
% rtresgev  - RTR driver for computing the extreme eigenspaces of a symmetric
%             matrix pencil
% rtrflat   - RTR driver for optimizing a (trivial) objective function
%             in R^n
%
% IRTR:
% irtrdsvd  - IRTR driver for computing the dominant singular triplets of a 
%             rectangalar matrix
% irtresgev - IRTR driver for computing the extreme eigenspaces of a symmetric 
%             matrix pencil
% tmesgev   - IRTR driver for computing the extreme eigenspaces of a symemtric
%             matrix pencil, in the manner of the Trace Minimization method

